    def _contract_with_pdf(self, scope=None):
        c = ContractFactory(scopes=[scope] if scope else [])
        c.file_content = _PDF_BYTES
        c.file_name = "deal.pdf"
        c.content_type = "application/pdf"
        c.save()
        return c

    def test_download_requires_login(self, client):
        c = self._contract_with_pdf()
        resp = client.get(reverse("assets:contract-document", kwargs={"pk": c.pk}))
        assert resp.status_code == 302  # redirected to login

    def test_download_returns_pdf(self, client):
        client.force_login(UserFactory(is_superuser=True, is_staff=True))
        c = self._contract_with_pdf()
        resp = client.get(reverse("assets:contract-document", kwargs={"pk": c.pk}))
        assert resp.status_code == 200
        assert resp["Content-Type"] == "application/pdf"
        assert "deal.pdf" in resp["Content-Disposition"]
        assert resp.getvalue() == _PDF_BYTES

    def test_download_404_without_document(self, client):
        client.force_login(UserFactory(is_superuser=True, is_staff=True))
        c = ContractFactory()
        resp = client.get(reverse("assets:contract-document", kwargs={"pk": c.pk}))
        assert resp.status_code == 404

    def test_download_scope_restricted(self, client):
        from accounts.models import Group, Permission

        scope_a = ScopeFactory()
        scope_b = ScopeFactory()
        reader = UserFactory()
        grp = Group.objects.create(name="ScopedContractReaders")
        grp.permissions.add(Permission.objects.get(codename="assets.contract.read"))
        grp.allowed_scopes.add(scope_a)
        grp.users.add(reader)
        client.force_login(reader)

        in_scope = self._contract_with_pdf(scope=scope_a)
        out_scope = self._contract_with_pdf(scope=scope_b)
        assert client.get(reverse("assets:contract-document", kwargs={"pk": in_scope.pk})).status_code == 200
        assert client.get(reverse("assets:contract-document", kwargs={"pk": out_scope.pk})).status_code == 404


