@pytest.mark.django_db
class TestContractLifecycle:
    def test_reference_autogenerated(self):
        c = Contract.objects.create(label="Auto ref")
        assert c.reference.startswith("CTRT-")

    def test_lifecycle_name_and_initial_step(self):
        c = Contract.objects.create(label="WF", status=ContractStatus.DRAFT)
        assert c.get_lifecycle().name == "contract"
        assert c.workflow_state == "draft"

    def test_available_transitions_from_draft(self):
        # Draft (generic entry) is distinct from "Contract draft": the first
        # move is into drafting; archive is always available.
        c = Contract.objects.create(label="WF", status=ContractStatus.DRAFT)
        targets = {t.target for t in c.available_transitions()}
        assert targets == {"drafting", "archived"}

    def test_entry_chain_and_review_cycle(self):
        c = Contract.objects.create(label="WF", status=ContractStatus.DRAFT)
        c.transition_to("drafting")   # Start drafting
        c.transition_to("signing")    # Send for signature
        c.transition_to("active")     # Bring into force
        c.refresh_from_db()
        assert c.workflow_state == "active"
        assert c.status == "active"  # legacy status kept in sync
        # In force <-> Under review recurring cycle.
        assert {t.target for t in c.available_transitions()} == {"under_review", "expired", "archived"}
        c.transition_to("under_review")
        assert "active" in {t.target for t in c.available_transitions()}  # Reviewed
        c.transition_to("active")
        c.refresh_from_db()
        assert c.workflow_state == "active"

    def test_archive_from_any_state_without_comment(self):
        # The lifecycle stepper UI collects no comment, so Archive must not be
        # comment-gated (otherwise the transition silently fails).
        for start in ["drafting", "signing", "active", "expired"]:
            c = Contract.objects.create(label=f"WF {start}", status=start)
            assert "archived" in {t.target for t in c.available_transitions()}
            c.transition_to("archived")  # no comment provided
            c.refresh_from_db()
            assert c.workflow_state == "archived"
            assert c.status == "archived"

    def test_archive_via_transition_endpoint(self, client):
        # Reproduce the stepper action: the lifecycle form POSTs target_status
        # only (no comment). Archive must move the contract to archived.
        client.force_login(UserFactory(is_superuser=True, is_staff=True))
        c = ContractFactory(status="active", scopes=[ScopeFactory()])
        url = reverse(
            "workflow:transition",
            kwargs={"app_label": "assets", "model": "contract", "pk": c.pk},
        )
        resp = client.post(url, {"target_status": "archived"})
        assert resp.status_code == 302
        c.refresh_from_db()
        assert c.workflow_state == "archived"
        assert c.status == "archived"

    def test_expired_cannot_be_renewed_in_place(self):
        from core.lifecycle import IllegalTransitionError

        c = Contract.objects.create(label="WF", status="expired")
        # No expired -> active : an expired contract is replaced (supersedes).
        with pytest.raises(IllegalTransitionError):
            c.transition_to("active")
        assert {t.target for t in c.available_transitions()} == {"archived"}

    def test_illegal_transition_raises(self):
        from core.lifecycle import IllegalTransitionError

        c = Contract.objects.create(label="WF", status=ContractStatus.DRAFT)
        with pytest.raises(IllegalTransitionError):
            c.transition_to("active")  # draft must go through "signing" first

    def test_draft_is_deletable_active_is_not(self):
        from core.lifecycle import LifecycleProtectedError

        draft = Contract.objects.create(label="D", status=ContractStatus.DRAFT)
        draft.delete()  # allowed
        active = Contract.objects.create(label="A", status=ContractStatus.ACTIVE)
        with pytest.raises(LifecycleProtectedError):
            active.delete()


