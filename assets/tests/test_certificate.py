@pytest.mark.django_db
class TestCertificateSupersede:
    def test_supersedes_relationship(self):
        old = CertificateFactory()
        new = CertificateFactory(supersedes=old)
        assert new.supersedes == old
        assert old.is_superseded is True
        assert new.is_superseded is False
        assert list(old.superseded_by.all()) == [new]

    def test_supersedes_set_null_on_replacement_delete(self):
        old = CertificateFactory(status=CertificateStatus.DRAFT)
        new = CertificateFactory(status=CertificateStatus.DRAFT, supersedes=old)
        old.delete()  # draft -> deletable; SET_NULL keeps the survivor
        new.refresh_from_db()
        assert new.supersedes is None

    def test_detail_shows_supersede_both_directions(self, client):
        client.force_login(UserFactory(is_superuser=True, is_staff=True))
        old = CertificateFactory(reference="CERT-200", label="Old cert")
        new = CertificateFactory(reference="CERT-201", label="New cert", supersedes=old)
        resp_old = client.get(reverse("assets:certificate-detail", kwargs={"pk": old.pk}))
        assert b"CERT-201" in resp_old.content  # replaced by
        resp_new = client.get(reverse("assets:certificate-detail", kwargs={"pk": new.pk}))
        assert b"CERT-200" in resp_new.content  # renews and replaces


