@pytest.mark.django_db
class TestCertificateCreateView:
    def _login(self, client):
        client.force_login(UserFactory(is_superuser=True, is_staff=True))

    def _base_data(self, scope):
        return {"label": "ISO 27001", "framework": str(FrameworkFactory().pk),
                "status": "draft", "scopes": [str(scope.pk)]}

    def test_scope_is_required(self, client):
        self._login(client)
        resp = client.post(reverse("assets:certificate-create"),
                           {"label": "X", "framework": str(FrameworkFactory().pk), "status": "draft"})
        assert resp.status_code == 200  # re-rendered with errors
        assert Certificate.objects.count() == 0

    def test_rejects_non_pdf_extension(self, client):
        self._login(client)
        scope = ScopeFactory()
        upload = SimpleUploadedFile("c.txt", _PDF_BYTES, content_type="text/plain")
        data = {**self._base_data(scope), "upload": upload}
        resp = client.post(reverse("assets:certificate-create"), data)
        assert resp.status_code == 200
        assert Certificate.objects.count() == 0

    def test_rejects_bad_magic_bytes(self, client):
        self._login(client)
        scope = ScopeFactory()
        upload = SimpleUploadedFile("c.pdf", b"NOT-A-PDF", content_type="application/pdf")
        data = {**self._base_data(scope), "upload": upload}
        resp = client.post(reverse("assets:certificate-create"), data)
        assert resp.status_code == 200
        assert Certificate.objects.count() == 0

    def test_accepts_valid_pdf(self, client):
        self._login(client)
        scope = ScopeFactory()
        upload = SimpleUploadedFile("c.pdf", _PDF_BYTES, content_type="application/pdf")
        data = {**self._base_data(scope), "upload": upload}
        resp = client.post(reverse("assets:certificate-create"), data)
        assert resp.status_code == 302
        certificate = Certificate.objects.get()
        assert certificate.has_document
        assert certificate.file_name == "c.pdf"
        assert certificate.get_file_bytes() == _PDF_BYTES
        assert list(certificate.scopes.all()) == [scope]

    def test_create_without_document(self, client):
        self._login(client)
        scope = ScopeFactory()
        resp = client.post(reverse("assets:certificate-create"), self._base_data(scope))
        assert resp.status_code == 302
        assert Certificate.objects.get().has_document is False


